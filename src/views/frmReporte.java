    private void Reporte(){
        
        Connection cn =DBConn.getConnection();
        String empresa="CASA ALFAJOR S.A.C.";       
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat sdf1=new SimpleDateFormat("dd/MM/yyyy");
        String fdesde=sdf.format(dtDesde.getDate());
        String fhasta=sdf.format(dtHasta.getDate());
        String finicio=sdf1.format(dtDesde.getDate());
        String fechafin=sdf1.format(dtHasta.getDate());
        
        
       Map parametros=new HashMap();
       parametros.put("desde", fdesde);
       parametros.put("hasta", fhasta);
       parametros.put("fecha1",finicio);
       parametros.put("fecha2",fechafin);
       parametros.put("Empresa",empresa);
             
        try {
            
            JasperReport reporte=JasperCompileManager.compileReport(this.getClass().getClassLoader().getResourceAsStream("views/pedidos.jrxml"));
            JasperPrint print = JasperFillManager.fillReport(reporte, parametros, cn);
            VenatanaReporte(print);
             
        } catch (Exception e) {
            System.out.println(e.toString());
        }
       
      
    }
