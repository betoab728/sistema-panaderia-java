    private void VenatanaReporte(JasperPrint print){
 
        try {
            
            JInternalFrame frame= new JInternalFrame("Reporte de Pedidos");
            frame.getContentPane().add(new JRViewer(print));
            frame.pack();
            frame.setResizable(true);
            frame.setClosable(true);
            frame.setMaximizable(true);
            frame.setSize(1200,600);
            MainForm.mainDesktop.add(frame);
            CentrarVentana(frame);
            frame.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
         
    } 
    
     private void CentrarVentana(JInternalFrame internalFrame) {
        Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();
       
         
        int x= (MainForm.mainDesktop.getWidth()/2)-internalFrame.getWidth()/2;
        int y=(MainForm.mainDesktop.getHeight()/2)-internalFrame.getHeight()/2;
        internalFrame.setLocation(x, y);

    }

